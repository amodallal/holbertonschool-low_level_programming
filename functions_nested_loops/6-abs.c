/**
 * _abs - Checks if a character is lowercase.
 * @n: The character to check.
 *
 * Description: This function checks if the input character `c`
 * computes the absolute value of an integer
 *
 *
 * Return: 1 if `c` is lowercase, 0 otherwise.
 */

int _abs(int n)
{
	if (n > 0)
	{
		n = n;
	}
	else if (n == 0)
	{
		n = 0;
	}
	else
	{
		n = n * -1;
	}
return (n);	
}
